package JAVA_CLASS_2;

public class UnaryOperatorTest {
    public static void main(String[] args) {
        int mark=75;
        mark=mark+1;
        mark=mark-1;
        System.out.println(mark++);
        System.out.println(mark);
        System.out.println(--mark);
        System.out.println(mark);
        System.out.println(mark--);
        System.out.println(++mark);
        System.out.println(mark);
    }
}
